class Map:
    def __init__(self, game):
        self.game = game
        self.mini_map = mini_map
        self.world_map = {}
        self.get_map()

    def get_map(self):
        for y, row in enumerate(self.mini_map):
            for x, tile in enumerate(row):
                if tile == 1:
                    self.world_map[(x, y)] = 1
                    
                elif tile == 2:
                    self.world_map[(x, y)] = 2
                    
                elif tile == 3:
                    self.world_map[(x, y)] = 3
                    
                elif tile == 4:
                    self.world_map[(x, y)] = 4
                    
                elif tile == 5:
                    self.world_map[(x, y)] = 5
                    
                

    def draw(self):
        for (x, y) in self.world_map:
            pg.draw.rect(
                self.game.screen,
                'darkgray',
                (x * 100, y * 100, 100, 100),
                2
            )